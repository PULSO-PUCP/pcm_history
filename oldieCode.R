% \subsection{The Instability Itself Is the Institution}
% 
% The most theoretically significant finding is not the variation 
% across epochs but the mechanism that explains the reproduction of 
% poor continuity conditions across them. The escape valve is not 
% simply a symptom of weak institutions. It is itself an institution 
% --- informal, durable, and self-reinforcing. Each time a minister 
% is sacrificed to relieve political pressure, the expectation that 
% dismissal is the appropriate response to crisis is reinforced. 
% The political cost of \emph{not} activating the escape valve rises. 
% The informal institution deepens its lock-in.
% 
% This finding flips the conventional interpretation of Peruvian 
% political instability. Most accounts treat chronic ministerial 
% turnover as evidence of institutional weakness --- the absence of 
% stable rules, norms, and enforcement mechanisms. The present 
% analysis suggests the opposite: the turnover is stable precisely 
% because it is institutionalized. What persists across two centuries 
% is not the absence of institutions but the presence of an informal 
% one whose purpose is to absorb political pressure without requiring 
% structural change.
% 
% Tomasi di Lampedusa captured the logic before political science 
% named it: \textit{``Se vogliamo che tutto rimanga com'è, bisogna 
  % che tutto cambi''} --- if we want everything to remain as it is, 
% everything must change. Peru's political class has learned this 
% lesson well. Governments change. Cabinets change. Constitutions 
% change. The repertoire through which instability is managed 
% remains largely intact. The minister is Tancredi's gesture: a 
% sacrifice offered so that the system does not have to transform 
% itself.
% 
% \subsection{The Protective Belt as Proof of the Conditional}
% 
% The protective belt finding does more than identify an exception 
% to the dominant pattern. It provides the critical theoretical test 
% that distinguishes between two interpretations of Peru's chronic 
% instability.
% 
% The first interpretation holds that instability is culturally or 
% structurally inevitable --- an expression of deep social 
% fragmentation, weak civic capacity, and the absence of the 
% rationality preconditions that would allow competence-based 
% ministerial evaluation. On this reading, Peru cannot produce 
% sustained ministerial continuity because the conditions for doing 
% so do not exist.
% 
% The second interpretation holds that instability is institutionally 
% produced --- the outcome of a specific cost structure in which 
% dismissal is cheap and continuity is not rewarded. On this reading, 
% Peru can produce sustained continuity wherever the cost structure 
% changes, and the escape valve is not a cultural inevitability but 
% a political equilibrium that can be disrupted by external forces.
% 
% The Economy portfolio in the Democracy without Parties epoch 
% supports the second interpretation. When international creditors, 
% sovereign debt markets, fiscal rules, and multilateral oversight 
% raised the cost of Economy minister replacement beyond what 
% domestic political pressure could sustain, continuity improved --- 
% not because Peruvian political culture changed, not because 
% presidents became more committed to institutional stability, but 
% because the incentive structure changed. The belt is not a 
% cultural achievement. It is a cost structure imposed from outside.
% 
% This finding has implications beyond Peru. It suggests that 
% protective belts --- localized continuity-preserving mechanisms 
% generated by external accountability --- may operate in any 
% executive branch where certain portfolios are embedded in 
% international financial or diplomatic networks. The mechanism is 
% not specific to the Peruvian case. What is specific to Peru is 
% the contrast between the narrow sector where the belt operates 
% and the vast majority of the state where it does not.
% 
% \subsection{What the Democratic Epoch Reveals}
% 
% The partial regression of continuity conditions in the Democracy 
% without Parties epoch deserves specific attention. The data show 
% that the most recent democratic period --- characterized by 
% universal suffrage, competitive elections, constitutional 
% continuity, and macroeconomic stability --- produced worse 
% continuity conditions than the military and power-sharing periods 
% that preceded it. This finding will appear paradoxical to readers 
% who expect democracy to improve institutional quality.
% 
% The paradox dissolves once the escape valve logic is applied. 
% Democratic accountability in a system without programmatic parties 
% creates strong incentives for ministerial dismissal. Presidents 
% facing fragmented congresses, high electoral volatility, and 
% personalist political competition cannot build stable coalitions 
% through policy commitments. What they can do is demonstrate 
% responsiveness --- and the most visible form of responsiveness 
% available is the ministerial reshuffle. Democracy without parties 
% does not weaken the escape valve. It intensifies it, because the 
% political cost of \emph{not} reshuffling in response to pressure 
% is higher when electoral survival depends on visible performance 
% signals rather than on party loyalty.
% 
% The 11.4 percent of spells ending within thirty days in the 
% democratic epoch --- nearly triple the rate of the Power-Sharing 
% epoch --- is the quantitative signature of this dynamic. Ministers 
% are appointed and removed before they can structure the problems 
% they were appointed to address, let alone convert their experience 
% into institutional knowledge. The knowledge spiral cannot start. 
% The organized anarchy reproduces itself. And the next crisis finds 
% the same ill-structured problems waiting, with the same absence 
% of accumulated knowledge, for the next minister to begin again 
% from zero.
% 
% \section{Conclusion}
% 
% This paper began with a question that two centuries of Peruvian 
% republican history make unavoidable: why has a political system 
% that has changed so much --- constitutions, regimes, coalitions, 
% economic models --- changed so little in the way it manages the 
% people responsible for governing? The answer developed here is 
% not that Peru lacks institutions. It is that Peru has an 
% institution --- the escape valve --- whose function is precisely 
% to absorb the pressure for change without producing it.
% 
% The analysis rests on an original dataset of 1,062 ministerial 
% spells covering five portfolios from 1821 to 2026, organized into 
% five institutional epochs defined by the critical junctures that 
% periodically restructured Peru's political logic. Ministerial 
% duration is treated not as an outcome to be explained but as a 
% signal --- an observable trace of the continuity conditions under 
% which governing experience either accumulates into institutional 
% knowledge or is repeatedly lost. The central empirical question 
% is whether Peru's critical junctures ever produced durable 
% improvements in those conditions. The answer is partial: one 
% juncture did, most did not, and the most recent democratic epoch 
% represents a partial regression toward the founding period's 
% pattern of chronic interruption.
% 
% Four conclusions follow from the analysis.
% 
% \textbf{First}, institutional epochs explain ministerial survival 
% far better than portfolio membership. The institutional period in 
% which a minister serves --- not their individual traits, not the 
% portfolio they hold --- is the primary determinant of how long 
% they remain in office. This finding situates the present analysis 
% within the ministerial survival literature while departing from 
% its standard assumptions: rather than asking what traits or 
% contextual factors predict individual duration, it asks what 
% patterns of duration reveal about the institutional environment 
% that produces them.
% 
% \textbf{Second}, Peru's critical junctures did not uniformly 
% improve continuity conditions. The largest and most durable shift 
% in the data --- a reduction of roughly fifty percent in exit 
% hazard --- occurred in the transition from the Oligarchic State 
% to the Power-Sharing State. The Transition epoch sustained those 
% conditions. The democratic epoch that followed Fujimori's 
% constitutional refoundation did not. Formal institutional change 
% and improvement in learning conditions have repeatedly failed 
% to coincide, consistent with the near-miss argument developed 
% in the theoretical framework.
% 
% \textbf{Third}, the instability itself is the institution. What 
% has been reproduced across two centuries is not the absence of 
% stable rules but the presence of an informal one: the expectation 
% that ministerial dismissal is the appropriate response to political 
% crisis, regardless of the minister's competence or the structural 
% nature of the problem. This informal escape valve is self-reinforcing 
% --- each activation raises the political cost of non-activation 
% and deepens the lock-in of the pattern. Path dependence in Peru 
% operates primarily at the informal level while the formal 
% institutional landscape churns.
% 
% \textbf{Fourth}, the protective belt is real but historically 
% specific. The Economy portfolio gained a significant survival 
% advantage only in the Democracy without Parties epoch, when 
% external accountability structures --- international creditors, 
% fiscal rules, sovereign debt markets, multilateral oversight --- 
% raised the cost of Economy minister replacement beyond what 
% domestic political pressure alone could sustain. In all earlier 
% epochs, Economy ministers faced the same exit hazard as Interior 
% ministers. The belt was not inherited from the portfolio's 
% institutional position. It was constructed by a specific 
% historical configuration of external constraints. And crucially, 
% it coexisted with the broader escape valve logic operating 
% everywhere else in the cabinet. The state did not escape the 
% learning trap. One corner of it did.
% 
% \subsection{Contributions and Limitations}
% 
% This paper makes three contributions to the existing literature. 
% The first is methodological: it introduces the institutional 
% epoch as the primary unit of historical comparison in ministerial 
% survival analysis, replacing the standard practice of pooling 
% spells across history or controlling for time as a continuous 
% covariate. The epoch framework allows survival analysis to detect 
% whether critical junctures produced durable changes in continuity 
% conditions --- a question the existing literature has not asked.
% 
% The second contribution is theoretical: it connects the 
% ministerial survival literature to organizational learning theory 
% through the concept of knowledge accumulation. Survival time is 
% reframed not as an individual outcome but as a systemic signal. 
% This reframing generates the protective belt concept as a natural 
% corollary: wherever external accountability raises the cost of 
% continuity interruption, a localized space for knowledge 
% accumulation emerges within a broader environment of institutional 
% instability.
% 
% The third contribution is empirical: two centuries of ministerial 
% duration data for Peru, organized into a dataset that did not 
% previously exist and that extends the Latin American ministerial 
% survival literature into the nineteenth century and into a case 
% that the comparative literature has largely treated as too 
% unstable for systematic analysis.
% 
% The analysis has limitations that define the agenda for future 
% work. The most fundamental is that organizational learning is not 
% directly observed. Ministerial duration is a proxy for the 
% continuity conditions from which learning may emerge, not a 
% measure of learning itself. Demonstrating that longer tenure 
% actually produced better policy outcomes, more effective 
% bureaucracies, or improved state capacity would require 
% ministry-level performance data that are not currently available 
% for the full historical period. Future work could extend the 
% analysis to include time-varying indicators of political pressure 
% --- protests, censure motions, approval ratings, executive-legislative 
% conflict --- to test the escape valve mechanism more directly. 
% It could also extend the protective belt argument to other 
% portfolios and other countries where external accountability 
% structures vary, generating comparative leverage on the 
% conditions under which belts form and dissolve.
% 
% \subsection{A Final Observation}
% 
% The France--Peru comparison with which this paper opened has a 
% resolution that the data make visible. France broke its path 
% at a critical juncture that produced not merely new institutions 
% but new continuity conditions --- a durable shift in how long 
% premiers serve and therefore in how much governing experience 
% can accumulate. Peru has had the junctures without the shift. 
% The disruption nexus arrived repeatedly. The ideas lying around 
% were the same ones that had failed before. And the escape valve 
% absorbed the pressure each time, ensuring that the system could 
% change everything --- governments, cabinets, constitutions --- 
  % so that the informal institution at its core could remain 
% exactly as it was.
% 
% Whether Peru's sixth critical juncture, still unfolding as this 
% paper goes to press, will produce a different outcome remains 
% genuinely open. The data cannot answer that question. What they 
% can say is that the conditions for a different answer have never 
% been more visible --- or more contested.
