% \subsection{Institutional Change Without Institutional Learning}
% 
% The central finding of this study is that Peru's critical junctures 
  % did not produce monotonic improvements in the conditions governing 
  % ministerial continuity. Two centuries of republican history, five 
  % institutional epochs, and 1,062 ministerial spells later, the most 
  % recent democratic period generates continuity conditions closer to 
  % the founding epoch than to the mid-century peak. This is not a 
  % story of gradual institutional development interrupted by occasional 
  % setbacks. It is a story of structural reproduction interrupted by 
  % one genuine improvement --- the Power-Sharing State --- that 
  % subsequent critical junctures failed to sustain.
  % 
  % The distinction between institutional change and institutional 
  % learning, introduced in the theoretical framework, finds its 
  % clearest empirical expression here. Peru has changed its 
  % institutions repeatedly --- thirteen constitutions, multiple regime 
  % transitions, democratic openings and authoritarian closures. Yet 
  % the continuity conditions under which ministerial experience 
  % accumulates have not improved in any consistent direction. The 
  % Oligarchic State, despite representing a period of formal 
  % reconstruction after the humiliation of the War of the Pacific, 
  % produced median ministerial durations almost identical to the 
  % founding epoch. The constitutional refoundation of 1993, despite 
  % introducing macroeconomic stabilization and a new institutional 
  % framework, failed to sustain the continuity conditions that the 
  % Power-Sharing and Transition epochs had produced. Formal 
  % institutional change and improvement in learning conditions have 
  % repeatedly failed to coincide.
  % 
  % This pattern is precisely what the near-miss framework predicts. 
  % Each major rupture --- the War of the Pacific, the Crisis of the 
  % Aristocratic Republic, the Military Revolution, Fujimori's 
% self-coup --- represented a moment of genuine contingency in which 
% alternative institutional trajectories were plausible. In each 
% case, the informal mechanism through which political pressure is 
% absorbed --- ministerial dismissal as the escape valve --- survived 
% the juncture and was reproduced in the subsequent path. The 
% formal rules changed. The informal institution did not.
